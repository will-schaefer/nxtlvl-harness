#!/usr/bin/env node
/**
 * ledger.ts — regenerate ledger.md from every cell manifest.
 *
 * `npm run ledger` wraps this. It is the single-glance view of the lab: one row per cell
 * (name · type · stage · target), each row a POINTER to cells/<name>/manifest.yaml — never a
 * paste of cell content (pointers-over-content, spec Code Style).
 *
 * Split for testability (matches manifest.ts / the PM plan-parser split):
 *   readCells(cellsDir) -> [{ name, type, stage, target, dir, ok }]   touches the filesystem
 *   renderLedger(cells) -> markdown string                            pure & deterministic
 * The golden test drives both against bin/__fixtures__.
 *
 * Fails open: a read error on one cell degrades that row (shows ⚠) rather than crashing the run.
 */

import * as fs from 'node:fs';
import * as path from 'node:path';
import * as m from './lib/manifest.ts';

/** One ledger row — a pointer to a cell's manifest, never its content. */
export interface LedgerRow {
  name: string;
  type: string;
  stage: string;
  target: string;
  dir: string;
  ok: boolean;
}

const LAB_ROOT = path.join(import.meta.dirname, '..');
export const CELLS_DIR = path.join(LAB_ROOT, 'cells');
export const LEDGER_PATH = path.join(LAB_ROOT, 'ledger.md');

export const HEADER = '<!-- GENERATED by bin/ledger.ts — do not hand-edit. Run `npm run ledger` to regenerate. -->';

/** Read every cells/<name>/manifest.yaml. Returns rows sorted by cell name. Never throws. */
export function readCells(cellsDir: string): LedgerRow[] {
  let entries: fs.Dirent[];
  try {
    entries = fs.readdirSync(cellsDir, { withFileTypes: true });
  } catch (_e) {
    return [];
  }
  const rows: LedgerRow[] = [];
  for (const ent of entries) {
    if (!ent.isDirectory()) continue;
    const dir = ent.name;
    const manifestPath = path.join(cellsDir, dir, 'manifest.yaml');
    let row: LedgerRow = { name: dir, type: '—', stage: '—', target: '—', dir, ok: false };
    try {
      const text = fs.readFileSync(manifestPath, 'utf8');
      const { manifest, errors } = m.validateText(text);
      if (manifest && typeof manifest === 'object') {
        const mf = manifest as Record<string, unknown>;
        row = {
          name: m_str(mf.name) || dir,
          type: m_str(mf.type) || '—',
          stage: m_str(mf.stage) || '—',
          target: m_str(mf.target) || '—',
          dir,
          // "ok" = structurally sound enough to track; author-owed gaps still count as ok.
          ok: errors.length === 0 || m.onlyAuthorOwed(errors),
        };
      }
    } catch (_e) {
      // No manifest / unreadable — degrade the row, don't crash.
    }
    rows.push(row);
  }
  rows.sort((a, b) => a.name.localeCompare(b.name));
  return rows;
}

function m_str(v: unknown): string {
  return typeof v === 'string' && v.trim() ? v.trim() : '';
}

/** Pure: rows -> ledger markdown. Deterministic. */
export function renderLedger(cells: LedgerRow[]): string {
  const lines = [HEADER, '# harness-lab ledger', ''];
  if (cells.length === 0) {
    lines.push('_No cells yet. Scaffold one with `node bin/new-cell.ts <name> --type=<type>`, then run `npm run ledger`._');
    lines.push('');
    return lines.join('\n');
  }
  lines.push('| Cell | Type | Stage | Target | Manifest |');
  lines.push('|------|------|-------|--------|----------|');
  for (const c of cells) {
    const flag = c.ok ? '' : '⚠ ';
    const pointer = `[manifest](cells/${c.dir}/manifest.yaml)`;
    lines.push(`| ${flag}${c.name} | ${c.type} | ${c.stage} | ${c.target} | ${pointer} |`);
  }
  lines.push('');
  const warned = cells.filter((c) => !c.ok).length;
  lines.push(`_${cells.length} cell${cells.length === 1 ? '' : 's'}${warned ? `, ${warned} with manifest issues (⚠)` : ''}._`);
  lines.push('');
  return lines.join('\n');
}

function main(): void {
  const cells = readCells(CELLS_DIR);
  const md = renderLedger(cells);
  fs.writeFileSync(LEDGER_PATH, md);
  process.stdout.write(`ledger.md regenerated — ${cells.length} cell(s).\n`);
  process.exit(0);
}

if (import.meta.main) {
  main();
}
