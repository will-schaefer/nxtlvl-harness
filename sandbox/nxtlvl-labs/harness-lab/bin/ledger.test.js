'use strict';
/**
 * Golden tests for ledger.js. Run: node --test bin/ledger.test.js
 *
 * Drives readCells() against the committed bin/__fixtures__/cells set and asserts the rendered
 * ledger markdown byte-for-byte. Hermetic — independent of whatever real cells/ contains.
 *
 * Fixtures: alpha-skill (valid, develop), beta-agent (valid, pressure-test),
 * gamma-broken (type: widget -> a real error -> ⚠ flag).
 */

const { test } = require('node:test');
const assert = require('node:assert');
const path = require('path');

const { readCells, renderLedger } = require('./ledger.js');

const FIXTURE_CELLS = path.join(__dirname, '__fixtures__', 'cells');

const HEADER = '<!-- GENERATED by bin/ledger.js — do not hand-edit. Run `npm run ledger` to regenerate. -->';

test('readCells reads, sorts by name, and flags invalid manifests', () => {
  const cells = readCells(FIXTURE_CELLS);
  assert.deepStrictEqual(cells.map((c) => c.name), ['alpha-skill', 'beta-agent', 'gamma-broken']);
  assert.strictEqual(cells[0].ok, true);
  assert.strictEqual(cells[1].ok, true);
  assert.strictEqual(cells[2].ok, false); // gamma-broken: unknown type
});

test('renderLedger produces the exact golden markdown for the fixture set', () => {
  const cells = readCells(FIXTURE_CELLS);
  const expected = [
    HEADER,
    '# harness-lab ledger',
    '',
    '| Cell | Type | Stage | Target | Manifest |',
    '|------|------|-------|--------|----------|',
    '| alpha-skill | skill | develop | plugins/nxtlvl/skills/alpha-skill | [manifest](cells/alpha-skill/manifest.yaml) |',
    '| beta-agent | agent | pressure-test | plugins/nxtlvl/agents/beta-agent | [manifest](cells/beta-agent/manifest.yaml) |',
    '| ⚠ gamma-broken | widget | develop | plugins/nxtlvl/skills/gamma-broken | [manifest](cells/gamma-broken/manifest.yaml) |',
    '',
    '_3 cells, 1 with manifest issues (⚠)._',
    '',
  ].join('\n');
  assert.strictEqual(renderLedger(cells), expected);
});

test('renderLedger emits the empty-state when there are no cells', () => {
  const md = renderLedger([]);
  assert.match(md, /_No cells yet\./);
  assert.ok(md.startsWith(HEADER));
});

test('readCells on a missing directory returns [] (fails open)', () => {
  assert.deepStrictEqual(readCells(path.join(__dirname, '__no_such_dir__')), []);
});

test('ledger pastes no cell content — only manifest pointers', () => {
  const md = renderLedger(readCells(FIXTURE_CELLS));
  // The intent prose from fixtures must never appear in the ledger.
  assert.ok(!md.includes('fixture skill cell'), 'ledger leaked cell content');
  assert.ok(md.includes('cells/alpha-skill/manifest.yaml'), 'ledger missing pointer');
});
